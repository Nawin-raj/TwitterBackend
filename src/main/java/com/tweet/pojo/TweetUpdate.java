package com.tweet.pojo;

public class TweetUpdate {
	String message;

	public TweetUpdate(String message) {
		super();
		this.message = message;
	}

	public TweetUpdate() {
		super();
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}
	

}
